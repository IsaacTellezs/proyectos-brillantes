        de los usuarios, y el proceso de resolución de disputas</p>
    <br>
    <h2 class= "contenido">Política de Reembolso y Cancelación</h2>
    <p class="contenido-1">Los patrocinadores pueden solicitar un reembolso completo dentro 
        de los 30 días posteriores a la contribución. Los creadores pueden cancelar su campaña 
        antes de que finalice el plazo de financiamiento, en cuyo caso los patrocinadores 
        recibirán un reembolso completo.</p>
    <br>
    <h2 class= "contenido">Política de Verificación de Proyectos</h2>
    <p class="contenido-1">Antes de que una campaña se publique en nuestra plataforma, 
        revisamos cuidadosamente la autenticidad y la viabilidad del proyecto 
        para proteger a nuestros patrocinadores 
        y garantizar que los proyectos tengan una alta probabilidad de éxito</p>

    <br>

<h2 class= "contenido">Normas sobre Contenido</h2>
<p class="contenido-1">No se permite publicar contenido que sea ilegal, difamatorio, violento o que infrinja 
    los derechos de propiedad intelectual de terceros en nuestra plataforma de Proyectos Brillantes</p>

<br>
<h2 class= "contenido">Comisiones y Tarifas</h2>
<p class="contenido-1">Cobramos una comisión del 5% al 10% sobre los fondos recaudados por cada proyecto, 
    además de las tarifas de procesamiento de pagos estándar del 3%. Estas tarifas 
    se deducen automáticamente de los fondos recaudados.</p>

<br>

<h2 class= "contenido">Política de Comunicación</h2>
<p class="contenido-1">Los creadores pueden comunicarse con sus patrocinadores a través de la plataforma 
    de mensajería interna. Si surge una disputa, 
    nuestro equipo de soporte estará disponible para mediar y resolver el problema.</p>

<br>

<h2 class= "contenido">Política de Pago</h2>
<p class="contenido-1">Los pagos de los patrocinadores se procesan a través de una pasarela segura y 
    se desembolsan a los creadores una vez 
    que la campaña alcance su objetivo de financiamiento. 
    Los pagos fallidos se reintentarán automáticamente."</p>

<br>

<h2 class= "contenido">Restricciones Geográficas</h2>
<p class="contenido-1">Nuestra plataforma de Proyectos Brillantes está abierta para proyectos de todo el mundo, 
    pero los patrocinadores deben ser residentes de países en los que se admitan transacciones en línea</p>
<br>

<h2 class= "contenido">Política de Abuso</h2>
<p class="contenido-1">Cualquier forma de abuso, fraude o actividad maliciosa 
    será investigada y puede resultar en la suspensión de la cuenta 
    del usuario infractor y la retirada de la campaña."</p>

<br>

<h2 class= "contenido">Normativas Legales y Regulatorias</h2>
